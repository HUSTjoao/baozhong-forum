// 读取用户添加的大学（原始数据）
function readUserAddedUniversities(): University[] {
  if (typeof window === 'undefined') return []
  try {
    const userAdded = localStorage.getItem('userAddedUniversities')
    if (!userAdded) return []
    const parsed = JSON.parse(userAdded) as University[]
    return Array.isArray(parsed) ? parsed : []
  } catch (error) {
    console.error('Error loading user added universities:', error)
    return []
  }
}

// 获取所有大学的辅助函数（包括用户添加的，且仅返回已审核通过的用户大学）
export function getAllUniversities(): University[] {
  // 为每个大学自动添加logoUrl和解析province/city
  // 总是从 location 解析，确保数据一致性
  const universitiesWithLogos = universities.map(uni => {
    const { province, city } = parseLocation(uni.location)
    return {
      ...uni,
      logoUrl: uni.logoUrl || `/logos/${uni.id}.png`,
      province,
      city,
    }
  })
  
  if (typeof window === 'undefined') {
    return universitiesWithLogos
  }

  const userUniversitiesRaw = readUserAddedUniversities()
  // 仅公开展示已审核通过的用户大学（isApproved 未设置或 true 视为已通过）
  const approvedUserUniversities = userUniversitiesRaw.filter(
    (uni) => uni.isApproved !== false
  )

  const userUniversitiesWithLogos = approvedUserUniversities.map((uni) => {
    const { province, city } = parseLocation(uni.location)
    return {
      ...uni,
      logoUrl: uni.logoUrl || `/logos/${uni.id}.png`,
      province,
      city,
    }
  })

  return [...universitiesWithLogos, ...userUniversitiesWithLogos]
}

// 添加用户大学
export function addUserUniversity(university: Omit<University, 'id' | 'isUserAdded'>): University {
  const newUniversity: University = {
    ...university,
    id: `user-${Date.now()}-${Math.random().toString(36).substr(2, 9)}`,
    isUserAdded: true,
    // 新增大学默认处于待审核状态，管理员审核通过后才对所有用户可见
    isApproved: false,
    reviewStatus: 'pending',
  }
  
  if (typeof window !== 'undefined') {
    try {
      const existing = readUserAddedUniversities()
      existing.push(newUniversity)
      localStorage.setItem('userAddedUniversities', JSON.stringify(existing))
    } catch (error) {
      console.error('Error saving user added university:', error)
    }
  }
  
  return newUniversity
}

// 管理员：获取所有用户新增大学（包含待审核和已审核）
export function getUserAddedUniversities(): University[] {
  return readUserAddedUniversities()
}

// 管理员：审核通过某所用户新增大学
export function approveUserUniversity(universityId: string): boolean {
  if (typeof window === 'undefined') return false
  try {
    const list = readUserAddedUniversities()
    const index = list.findIndex((u) => u.id === universityId)
    if (index === -1) return false
    list[index] = { ...list[index], isApproved: true, reviewStatus: 'approved' }
    localStorage.setItem('userAddedUniversities', JSON.stringify(list))
    return true
  } catch (error) {
    console.error('Error approving university:', error)
    return false
  }
}

// 管理员：删除/拒绝某所用户新增大学
export function deleteUserUniversity(universityId: string, reason?: string): boolean {
  if (typeof window === 'undefined') return false
  try {
    const list = readUserAddedUniversities()
    const index = list.findIndex((u) => u.id === universityId)
    if (index === -1) return false
    // 标记为被拒绝，但保留记录，便于“我的申请”查看
    list[index] = { 
      ...list[index], 
      isApproved: false, 
      reviewStatus: 'rejected',
      rejectionReason: reason 
    }
    localStorage.setItem('userAddedUniversities', JSON.stringify(list))
    return true
  } catch (error) {
    console.error('Error deleting user university:', error)
    return false
  }
}

// 管理员：编辑用户提交的大学信息
export function updateUserUniversity(universityId: string, updates: Partial<University>): boolean {
  if (typeof window === 'undefined') return false
  try {
    const list = readUserAddedUniversities()
    const index = list.findIndex((u) => u.id === universityId)
    if (index === -1) return false
    
    list[index] = { ...list[index], ...updates }
    localStorage.setItem('userAddedUniversities', JSON.stringify(list))
    return true
  } catch (error) {
    console.error('Error updating user university:', error)
    return false
  }
}

