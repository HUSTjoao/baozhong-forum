export interface University {
  id: string
  name: string
  level: string
  location: string // 保留原有字段以保持向后兼容
  province?: string // 省份
  city?: string // 城市
  description: string
  students: number
  majors: string[]
  motto?: string // 校训
  logoUrl?: string // 校徽图片地址
  isUserAdded?: boolean // 标记是否为用户添加
  // 管理员审核状态：未设置或 true 视为已通过，仅用户新增的数据会用到该字段
  isApproved?: boolean
  // 审核状态：pending 待阅 / approved 已阅 / rejected 被拒
  reviewStatus?: 'pending' | 'approved' | 'rejected'
  // 拒绝原因
  rejectionReason?: string
  // 提交者
  creatorId?: string
}

// 解析location字符串为省-市结构
export function parseLocation(location: string): { province: string; city: string } {
  // 直辖市处理
  if (location === '北京市' || location === '上海市' || location === '天津市' || location === '重庆市') {
    return { province: location, city: location }
  }
  
  // 匹配格式：XX省XX市 或 XX自治区XX市 或 XX省XX市XX区
  // 使用非贪婪匹配，确保只匹配到第一个省/自治区/特别行政区
  const provinceMatch = location.match(/^(.+?(?:省|自治区|特别行政区))/)
  if (provinceMatch) {
    const province = provinceMatch[1].trim()
    // 提取城市部分（去掉省份）
    const cityPart = location.substring(province.length).trim()
    // 城市可能是：XX市、XX市XX区、XX州XX市等
    // 使用分组匹配城市名称（包含"市"、"州"、"县"、"区"、"盟"等后缀）
    if (cityPart) {
      const cityMatch = cityPart.match(/^(.+?(?:市|州|县|区|盟))/)
      const city = cityMatch ? cityMatch[1].trim() : cityPart.trim()
      return { province, city: city || location }
    }
    // 如果没有城市部分，返回省份作为城市（对于直辖市等特殊情况）
    return { province, city: province }
  }
  
  // 如果无法解析，返回原location作为city，province为空
  return { province: '', city: location }
}

// 生成ID的辅助函数
function generateId(name: string): string {
  return name
    .replace(/[（()）]/g, '')
    .replace(/\s+/g, '-')
    .toLowerCase()
    .replace(/[^a-z0-9\u4e00-\u9fa5-]/g, '')
}

